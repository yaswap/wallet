<template>
  <div class="import-wallet login-wrapper no-outer-pad">
    <div class="login-header">
      <LogoWallet />
    </div>
    <div class="import-wallet_top" id="import-wallet_top">
      <h2>{{ $t('pages.onboarding.import.unlockWallet') }}</h2>
      <p v-if="currentStep === 'seedphrase'">
        {{ $t('pages.onboarding.import.enterSeedPhrase') }}
      </p>
      <p v-else-if="currentStep === 'seedtype'">
        Choose the seed type
      </p>
      <p v-else>
        Input your custom derivation path
      </p>
    </div>
    <div class="import-wallet_bottom" v-if="currentStep === 'seedphrase'">
      <div class="btn-group" id="word_button_group">
        <button
          :class="{ active: numWords === 12 }"
          class="btn btn-option"
          id="twelve_words_option"
          @click="setMnemonicLength(12)"
        >
          {{ $t('pages.onboarding.import.words', { words: 12 }) }}
        </button>
        <button
          :class="{ active: numWords === 24 }"
          class="btn btn-option"
          id="twenty_four_words_option"
          @click="setMnemonicLength(24)"
        >
          {{ $t('pages.onboarding.import.words', { words: 24 }) }}
        </button>
      </div>
      <form class="form import-wallet_seed" autocomplete="off">
        <div v-for="(e, n) in numWords" :key="n">
          <input
            type="text"
            class="form-control form-control-sm"
            id="import_wallet_word"
            v-model="wordList[n]"
            autocomplete="off"
            required
          />
        </div>
      </form>
    </div>
    <div class="import-wallet_bottom" style="text-align: left" v-else-if="currentStep === 'seedtype'">
      <div class="custom-control custom-radio">
        <input
          class="custom-control-input"
          type="radio"
          name="seed_type_option"
          id="option_circbit_seed"
          v-model="seedtype"
          value="circbit"
        />
        <label class="custom-control-label" for="option_circbit_seed">
          Circbit seed (generated by Circbit app)
        </label>
      </div>
      <div class="custom-control custom-radio">
        <input
