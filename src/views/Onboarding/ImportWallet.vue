          id="option_bip39_standard_seed"
          v-model="seedtype"
          value="bip39_standard"
        />
        <label class="custom-control-label" for="option_bip39_standard_seed">
          Standard BIP39 seed (generated by Yaswap wallet)
        </label>
      </div>
      <div class="custom-control custom-radio">
        <input
          class="custom-control-input"
          type="radio"
          name="seed_type_option"
          id="option_bip39_custom_seed"
          v-model="seedtype"
          value="bip39_custom"
        />
        <label class="custom-control-label" for="option_bip39_custom_seed">
          BIP39 seed with custom derivation path
        </label>
      </div>
    </div>
    <div class="import-wallet_bottom" style="text-align: left" v-else>
      <div class="derivation-path-form-control">
        <input type="text" v-model="customDerivationPath" placeholder="m/84'/0'/0'" />
      </div>
    </div>
    <div class="footer-container bg-white">
      <div class="footer-content">
        <button
          id="import_wallet_cancel_button"
          class="btn btn-light btn-outline-primary btn-lg btn-footer btn-icon"
          @click="back"
        >
          {{ $t('common.back') }}
        </button>
        <button
          id="import_wallet_continue_button"
          class="btn btn-primary btn-lg btn-footer ml-2"
          :disabled="nextDisabled"
          @click="next"
        >
          {{ $t('common.continue') }}
        </button>
      </div>
    </div>
  </div>
</template>

<script>
import { mapState } from 'vuex'
import LogoWallet from '@/assets/icons/logo_wallet.svg'
import { validateMnemonic } from 'bip39'

export default {
  components: {
    LogoWallet
  },
  data: function () {
    return {
      currentStep: 'seedphrase',
      wordList: Array(12).fill(''),
      numWords: 12,
      seedtype: 'circbit',
      customDerivationPath: null,
    }
  },
  updated: function () {},
  watch: {
    wordList: function (newList) {
      var words = newList[0].split(' ')
      if (words.length === this.numWords) {
        for (var m = 0; m < words.length; m++) {
          this.wordList[m] = words[m]
        }
      }
    }
  },
  computed: {
    ...mapState(['wallets', 'activeWalletId']),
    wallet() {
      return this.wallets.find((wallet) => wallet.id === this.activeWalletId)
    },
    validMnemonic() {
      return (
        (this.mnemonic.split(' ').length === 12 || this.mnemonic.split(' ').length === 24) &&
        validateMnemonic(this.mnemonic)
      )
    },
    nextDisabled() {
      return this.mnemonic === '' || !this.validMnemonic
    },
    mnemonic() {
      return this.wordList.join(' ')
    }
  },
  methods: {
    back() {
      switch (this.currentStep) {
        case 'seedphrase':
        default:
          this.$router.push('/onboarding/home')
          break
        case 'seedtype':
          this.currentStep = 'seedphrase'
          break
        case 'derivationpath':
          this.currentStep = 'seedtype'
          break
      }
    },
    next() {
      switch (this.currentStep) {
        case 'seedphrase':
          this.currentStep = 'seedtype'
          break
        case 'seedtype':
          if (this.seedtype === 'bip39_custom') {
            this.currentStep = 'derivationpath'
          } else {
            this.gotoSetup()
          }
          break
        case 'derivationpath':
        default:
          this.gotoSetup()
          break
      }
    },
    gotoSetup() {
      let derivationPath = undefined
      switch (this.seedtype) {
        case 'circbit':
          derivationPath = "m/84'/0'/0'"
          break
        case 'bip39_custom':
          derivationPath = this.customDerivationPath
        case 'bip39_standard':
        default:
            break

      }

      this.$router.push({
        name: 'OnboardingSetup',
        params: { seedphrase: this.mnemonic, derivationPath }
      })
    },
    setMnemonicLength(words) {
      this.numWords = words
      this.wordList = Array(this.numWords).fill('')
    }
  }
}
</script>

